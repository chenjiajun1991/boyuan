package com.sam.yh.unicom.sim;

public class EditTerminalException extends Exception {

    private static final long serialVersionUID = -7156532349643672145L;

    public EditTerminalException() {
        super();
    }

    public EditTerminalException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public EditTerminalException(String message, Throwable cause) {
        super(message, cause);
    }

    public EditTerminalException(String message) {
        super(message);
    }

    public EditTerminalException(Throwable cause) {
        super(cause);
    }

}
