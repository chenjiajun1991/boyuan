package com.sam.yh.crud.exception;

public class BtyFollowException extends CrudException {

    public BtyFollowException() {
        super();
    }

    public BtyFollowException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public BtyFollowException(String message, Throwable cause) {
        super(message, cause);
    }

    public BtyFollowException(String message) {
        super(message);
    }

    public BtyFollowException(Throwable cause) {
        super(cause);
    }

}
