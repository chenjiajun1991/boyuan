package com.sam.yh.crud.exception;

public class FetchFollowerException extends CrudException {

    public FetchFollowerException() {
        super();
    }

    public FetchFollowerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public FetchFollowerException(String message, Throwable cause) {
        super(message, cause);
    }

    public FetchFollowerException(String message) {
        super(message);
    }

    public FetchFollowerException(Throwable cause) {
        super(cause);
    }

}
