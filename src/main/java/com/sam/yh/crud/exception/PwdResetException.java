package com.sam.yh.crud.exception;

public class PwdResetException extends CrudException {

    public PwdResetException() {
        super();
    }

    public PwdResetException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public PwdResetException(String message, Throwable cause) {
        super(message, cause);
    }

    public PwdResetException(String message) {
        super(message);
    }

    public PwdResetException(Throwable cause) {
        super(cause);
    }

}
