package com.sam.yh.crud.exception;

public class FetchResellerException extends CrudException {

    public FetchResellerException() {
        super();
    }

    public FetchResellerException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public FetchResellerException(String message, Throwable cause) {
        super(message, cause);
    }

    public FetchResellerException(String message) {
        super(message);
    }

    public FetchResellerException(Throwable cause) {
        super(cause);
    }

}
